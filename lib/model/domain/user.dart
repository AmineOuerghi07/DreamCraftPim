class User {
  final String fullname;
  final String email;
  final String password;
  final String phonenumber;
  final String address;
  final String role;
  final String cin;
  final String image;

  User({
    required this.fullname,
    required this.email,
    required this.password,
    required this.phonenumber,
    required this.address,
    required this.role,
    required this.cin,
    required this.image,
  });
}